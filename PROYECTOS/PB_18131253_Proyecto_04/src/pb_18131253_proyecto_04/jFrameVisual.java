    private void jButGrabarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButGrabarActionPerformed
        // TODO add your handling code here:
        int i = Integer.parseInt(jSpinPosicion.getValue()+"");
        
        modelo.setValueAt(i, i-1, 0);
        modelo.setValueAt(jTextLadoX.getText(), i-1, 1);
        modelo.setValueAt(jTextLadoY.getText(), i-1, 2);
        modelo.setValueAt(jTextLadoX2.getText(), i-1, 3);
        modelo.setValueAt(jTextLadoY2.getText(), i-1, 4);
       
        if(i<Integer.parseInt(jSpinNumero.getValue()+""))
            i++;
        else
            i=1;
        
        jSpinPosicion.setValue(i);
    }//GEN-LAST:event_jButGrabarActionPerformed

    private void jButAleatorioActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButAleatorioActionPerformed
        // TODO add your handling code here:
         for(int i=1; i<=Integer.parseInt(jSpinNumero.getValue()+"");i++)
        {
            
            modelo.setValueAt(i, i-1, 0);
            modelo.setValueAt(1+azar.nextInt(250), i-1, 1);
            modelo.setValueAt(1+azar.nextInt(250), i-1, 2);
            modelo.setValueAt(1+azar.nextInt(250), i-1, 3);
            modelo.setValueAt(+azar.nextInt(250), i-1, 4);
        }
        
         switch(azar.nextInt(8))
        {
            case 0: jRadioAmarillo.doClick();
            break;
            case 1: jRadioAzul.doClick();
            break;
            case 2: jRadioNaranja.doClick();
            break;
            case 3: jRadioRosa.doClick();
            break;
            case 4: jRadioRojo.doClick();
            break;
            case 5: jRadioVerde.doClick();
            break;
            case 6: jRadioGris.doClick();
            break;
            case 7: jRadioCyan.doClick();
            break;
                
        }
    }//GEN-LAST:event_jButAleatorioActionPerformed

