    private void jButGrabarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButGrabarActionPerformed
        // TODO add your handling code here:
      Prestamo pre = new Prestamo();
      
      pre.Nombre(jTextNombre.getText()); 
      pre.CapitalInicial(Double.parseDouble(jTextCapitalInicial.getText()));
      pre.Meses(Integer.parseInt(jTextMeses.getText()));
      pre.TasaAnual(Double.parseDouble(jTextTasaAnual.getText()));
                   
 
        int posicion = (int)jSpinPosicion.getValue() - 1;
        
        Compartamos.setPrestamo(pre, posicion);
        Compartamos.mostrar(jTableClientes, jLabCapitalTotal);
        
           if (jCheckBoxIncremento.isSelected())
            if (posicion==Compartamos.getTamaño()-1)
                jSpinPosicion.setValue(1);
            else
                jSpinPosicion.setValue(posicion+2);
  
        
    }//GEN-LAST:event_jButGrabarActionPerformed

