    private void jButCalcularActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButCalcularActionPerformed
        // TODO add your handling code here:
        //DECLARACION DE VARIABLES 
        double radio, altura, area, volumen;
        //DATOS DE ENTRADA
        radio=Double.parseDouble(jTextRadio.getText());
        altura=Double.parseDouble(jTextAltura.getText());
        //PROCESO
        volumen = Math.PI * Math.pow(radio,2) * altura;
        area = 2 * Math.PI * radio * (radio + altura);
        //DATOS DE SALIDA 
        jLabArea.setText(area+"");
        jLabVolumen.setText(volumen+"");
        
        //DESEAR QUE EL COLOR CORRESPONDIDO AL BOTON SE AGREGUE AL COLOR DEL FONDO DEL TEXTO DE AREA Y VOLUMEN:
        jLabArea.setBackground(jButCalcular.getBackground());
        jLabVolumen.setBackground(jButCalcular.getBackground());
    }//GEN-LAST:event_jButCalcularActionPerformed

