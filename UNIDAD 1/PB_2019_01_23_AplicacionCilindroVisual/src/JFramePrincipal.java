    public static double volumenCilindro(double r, double h)
    {
        double v;
        v=Math.PI * Math.pow(r,2) * h;
        return v;
    }
    public static double areaCilindro (double r, double h)
    {
       return 2 * Math.PI * r * (r + h);    
    }
