    private void jButCalcularActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButCalcularActionPerformed
        // TODO add your handling code here:
        Horario entrada, salida, tiempoTrabajado;
        
        Horario total = new Horario();
        
        for(int r = 0; r<modelo.getRowCount();r++)
        {
            entrada = new Horario((int)modelo.getValueAt(r, 1),
                                  (int)modelo.getValueAt(r, 2),
                                  (int)modelo.getValueAt(r,3));
            
            salida = new Horario(Integer.parseInt(modelo.getValueAt(r, 4)+""),
                                Integer.parseInt(modelo.getValueAt(r, 5)+""),
                                Integer.parseInt(modelo.getValueAt(r,6)+""));
            
            tiempoTrabajado = salida.resta(entrada);
            
            modelo.setValueAt(tiempoTrabajado, r, 7);
        
            total = total.suma(tiempoTrabajado);
            
        
        }
        
        jLabTotal.setText(total+"");
        
        
    }//GEN-LAST:event_jButCalcularActionPerformed

