    private void jButParalelosActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButParalelosActionPerformed
        // TODO add your handling code here:
        String [] nombres  = new String[modelo.getRowCount()];
        Horario [] entradas = new Horario[nombres.length];
        Horario [] salidas = new Horario [nombres.length];
        Horario [] tiempo = new Horario [nombres.length];
        
        for (int r = 0; r<modelo.getRowCount(); r++)
        {
            Horario ent = new Horario();
            Horario sal = new Horario();
            
            ent.setHoras((int)modelo.getValueAt(r, 1));
            ent.setMinutos((int)modelo.getValueAt(r, 2));
            ent.setSegundos((int)modelo.getValueAt(r, 3));
            
            sal.setHoras((int)modelo.getValueAt(r, 4));
            sal.setMinutos((int)modelo.getValueAt(r, 5));
            sal.setSegundos((int)modelo.getValueAt(r, 6));
          
            nombres[r] = modelo.getValueAt(r, 0)+"";
            entradas[r] = ent;
            salidas[r] = sal;
            tiempo [r] = sal.resta(ent);
        }

        for(int i = 0; i<tiempo.length-1; i++) // ordenando
            for(int j= i+1; j<tiempo.length; j++)
                if(tiempo[i].segundosTotales() > tiempo[j].segundosTotales())
                {
                    Horario aux = tiempo[i];
                    tiempo[i] = tiempo[j];
                    tiempo[j] = aux;
                    
                    aux = entradas[i]; // ordenar el arreglo de horario de entrada
                    entradas[i] = entradas[j];
                    entradas[j] = aux;
                    
                    aux = salidas[i];  // ordenar el arreglo de horario de salida
                    salidas[i] = salidas[j];
                    salidas[j] = aux;
                    
                    String auxn = nombres[i];   // ordenar el arreglo de los nombres
                    nombres[i] = nombres[j];
                    nombres[j] = auxn;
        
                }
        
        
        for(int r=0; r<modelo.getRowCount(); r++)
        {
            modelo.setValueAt(nombres[r], r, 0);
            
            modelo.setValueAt(entradas[r].getHoras(), r, 1);
            modelo.setValueAt(entradas[r].getMinutos(), r, 2);
            modelo.setValueAt(entradas[r].getSegundos(), r, 3);
            
            modelo.setValueAt(salidas[r].getHoras(), r, 4);
            modelo.setValueAt(salidas[r].getMinutos(), r, 5);
            modelo.setValueAt(salidas[r].getSegundos(), r, 6);
        
            modelo.setValueAt(tiempo[r], r, 7);
        }
        
        
        
        
    }//GEN-LAST:event_jButParalelosActionPerformed

