    public JFrameConsulta(JFrameMenu jfm) {
        initComponents();
        
        jfMenu = jfm;
        
        jLabEmpReg.setText(jfMenu.getEmpleadosRegistrados()+"");//PARA QUE APRAREZCAN LOS EMPLEADOS EN LA ETIQUETA
        
        modeloLista = new DefaultListModel(); //creando el objeto 
        //ARREGLO DE NOMBRES:
        String [] nom = new String[jfMenu.getEmpleadosRegistrados()];
        
        for (int i = 0; i < jfMenu.getEmpleadosRegistrados(); i++)
            nom[i] = jfMenu.getEmpleado(i).getNombre();
        
        Arrays.sort(nom);
        
        for (int i = 0; i < jfMenu.getEmpleadosRegistrados(); i++)
            modeloLista.addElement(nom[i]); //aÃ±ade el elemento de la lista de los nombres 
        
        jListEmpleados.setModel(modeloLista); //SE LE AGREGA LO DATOS DEL MODELO LISTA A LA LISTA EMPLEADOS
        
    }

