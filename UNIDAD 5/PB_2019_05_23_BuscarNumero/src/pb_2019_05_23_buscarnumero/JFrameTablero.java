    @Override
    public void actionPerformed(ActionEvent e) {
        MiBoton boton = (MiBoton)e.getSource();
        
        boton.mostrar();
        boton.setEnabled(false);
        
        intentos++;
        
        jLabIntentos.setText(intentos+"");
        
        if(boton.getNumero() == numeroABuscar)
        {
            JOptionPane.showMessageDialog(rootPane, "YA LO ENCONTRO");
            
            for(MiBoton b: fichas)
                b.mostrar();
        }
        
        
    }

