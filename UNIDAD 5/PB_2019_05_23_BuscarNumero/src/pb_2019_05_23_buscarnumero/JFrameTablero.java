    public JFrameTablero() {
        initComponents();
        
        int x=30;
        int y=50;
        
        setSize(550,550);
        
        MiBoton.setColor(2);
        
        for(int i=0; i<fichas.length; i++)
        {
            fichas[i] = new MiBoton(azar.nextInt(100));
        
            fichas[i].setLocation(x, y);
            

            x+=100;
            
            if((i+1)%5 == 0)
            {
                x = 30;
                y += 100;
            }
            
            fichas[i].addActionListener(this);
            
            add(fichas[i]);
            
          
        }
        
       numeroABuscar = fichas[azar.nextInt(fichas.length)].getNumero();
       intentos = 0;
       
       jLabBuscar.setText(numeroABuscar+"");
       jLabIntentos.setText(intentos+"");
       
    }

